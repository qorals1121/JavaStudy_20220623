package j01_출력;

public class HelloJava {
	// class명과 public class의 명이 같아야한다

	public static void main(String[] args) {
	    System.out.println("hello, Java");
	    System.out.println("백민경");
	    // java는 python과 다르게 들여쓰기가 없어도 실행이 가능하지만, 들여쓰기를 하지 않으면 구분하기가 힘들기 때문에 좋지않은 코드이다.
	    // 중괄호는 무조건 열리면 닫아야한다. 코드의 끝에는 ;(세미콜론)을 써야한다.
	    // 세미콜론으로 명령어를 끊어주기 때문에 한줄로 쭉 써도 상관은 없다.
	    
	    
	    System.out.print("이름: ");
	    System.out.println("김준일");
	    System.out.print("연락처: ");
	    System.out.println("010-9988-1916");

	}

}
